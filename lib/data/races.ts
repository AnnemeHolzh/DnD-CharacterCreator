// Helper function to get subraces for a specific race
export const getSubracesForRace = (raceId: string) => {
  const race = races.find(r => r.id === raceId)
  return race?.subraces || []
}

// Helper function to validate race and subrace combination
export const validateRaceSubraceCombination = (raceId: string, subraceId: string) => {
  const subraces = getSubracesForRace(raceId)
  return subraces.some(subrace => subrace.id === subraceId)
}
